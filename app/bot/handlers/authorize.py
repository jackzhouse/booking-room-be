"""
Handler for /authorize command.
Allows users to authenticate via a code generated by the frontend.
"""
from telegram import Update
from telegram.ext import ContextTypes

from app.services.auth_code_service import auth_code_service
from app.core.security import create_access_token
from app.core.config import settings
from app.models.user import User


async def create_or_update_user(telegram_user_data: dict) -> User:
    """
    Create or update user from Telegram data.
    """
    from datetime import datetime
    from app.core.config import settings
    from app.models.user import User
    
    telegram_id = telegram_user_data["id"]
    
    # Check if user exists
    user = await User.find_one(User.telegram_id == telegram_id)
    
    # Build full name
    first_name = telegram_user_data.get("first_name", "")
    last_name = telegram_user_data.get("last_name", "")
    full_name = f"{first_name} {last_name}".strip()
    
    if user:
        # Update existing user
        user.full_name = full_name
        user.username = telegram_user_data.get("username")
        user.avatar_url = telegram_user_data.get("photo_url")
        user.last_login_at = datetime.now(settings.timezone)
        
        # Set admin if this is the first admin
        if telegram_id == settings.ADMIN_TELEGRAM_ID and not user.is_admin:
            user.is_admin = True
        
        await user.save()
    else:
        # Create new user
        user = User(
            telegram_id=telegram_id,
            full_name=full_name,
            username=telegram_user_data.get("username"),
            avatar_url=telegram_user_data.get("photo_url"),
            is_admin=(telegram_id == settings.ADMIN_TELEGRAM_ID),
            is_active=True,
            last_login_at=datetime.now(settings.timezone)
        )
        await user.insert()
    
    return user


async def authorize_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    Handle /authorize command.
    
    Usage: /authorize <code>
    
    User generates a code from the frontend, then sends it to the bot.
    The bot verifies the code and associates the user's Telegram identity with it.
    """
    # Check if code is provided
    if not context.args or len(context.args) != 1:
        await update.message.reply_text(
            "❌ Please provide a 6-digit code.\n\n"
            "Usage: /authorize <code>\n\n"
            "Example: /authorize 123456"
        )
        return
    
    code = context.args[0]
    
    # Validate code format (6 digits)
    if not code.isdigit() or len(code) != 6:
        await update.message.reply_text(
            "❌ Invalid code format.\n\n"
            "Code must be a 6-digit number.\n\n"
            "Example: /authorize 123456"
        )
        return
    
    # Get user info from Telegram
    user = update.effective_user
    
    if not user:
        await update.message.reply_text("❌ Could not identify user.")
        return
    
    print(f"🔍 Bot: /authorize command received from user {user.id}")
    print(f"🔍 Bot: Code to verify: {code}")
    
    # Verify code using auth_code_service directly
    code_data = await auth_code_service.verify_code(code)
    
    if not code_data:
        print(f"🔍 Bot: Code {code} is invalid or expired")
        await update.message.reply_text(
            "❌ Invalid or expired code.\n\n"
            "Please generate a new code from the frontend and try again."
        )
        return
    
    print(f"🔍 Bot: Code {code} is valid")
    
    # Prepare Telegram user data
    telegram_user_data = {
        "id": user.id,
        "first_name": user.first_name or "",
        "last_name": user.last_name or "",
        "username": user.username,
        "photo_url": None
    }
    
    # Mark code as used with user data
    await auth_code_service.mark_code_used(code, telegram_user_data)
    print(f"🔍 Bot: Code {code} marked as used")
    
    # Create or update user
    user_obj = await create_or_update_user(telegram_user_data)
    print(f"🔍 Bot: User created/updated: {user_obj.id}")
    
    # Generate JWT token
    access_token = create_access_token(data={"sub": str(user_obj.id)})
    print(f"🔍 Bot: Token generated successfully")
    
    # Build full name
    full_name = user_obj.full_name or "User"
    
    # Build success message (simplified, in Indonesian, without token)
    message = (
        f"✅ Otorisasi berhasil!\n\n"
        f"Selamat datang, {full_name}!\n\n"
        f"Anda akan otomatis login ke dashboard aplikasi.\n\n"
        f"🔗 Buka aplikasi: {settings.FRONTEND_URL}"
    )
    
    if user_obj.is_admin:
        message += "\n\n🔑 Anda memiliki hak akses admin."
    
    await update.message.reply_text(message)
    
    print(f"🔍 Bot: Authorization successful for user {user.id}")
